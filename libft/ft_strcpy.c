#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*cpy;

	cpy = &*dst;
	while (*src)
	{
		*dst = *(char *)src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (cpy);
}
