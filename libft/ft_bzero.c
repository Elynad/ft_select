#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char		*cpy;
	size_t				i;

	cpy = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (cpy[i] != 0)
			cpy[i] = 0;
		i++;
	}
}
